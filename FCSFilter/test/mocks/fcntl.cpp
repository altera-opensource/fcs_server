#include "fcntl.h"

#define FCS_DEVICE_PATH "/dev/fcs"

int open(const char* file, int flags)
{
    Logger::log("open() mock called", Debug);
    if (std::string(file) != std::string(FCS_DEVICE_PATH) || flags != O_RDWR)
    {
        return -1;
    }
    return MOCK_FILE_DESCRIPTOR;
}
